function eGovUrl(egov) {
  return "https://laws.e-gov.go.jp/law/" + egov;
}

function findArticle(lawId, rawInput) {
  var law = LAWS.find(function(l) { return l.id === lawId; });
  if (!law) return null;
  var s = String(rawInput).trim();
  var ldb = DB[lawId] || {};
  if (ldb[s]) {
    var a = ldb[s];
    return { found:true, lawName:law.name, lawShort:law.short, lawEgov:law.egov,
             article:s, t:a.t, s:a.s, p:a.p, c:a.c, ex:a.ex };
  }
  var n = parseInt(s, 10);
  if (isNaN(n)) return null;
  if (ldb[n]) {
    var b = ldb[n];
    return { found:true, lawName:law.name, lawShort:law.short, lawEgov:law.egov,
             article:n, t:b.t, s:b.s, p:b.p, c:b.c, ex:b.ex };
  }
  return { found:false, lawName:law.name, lawShort:law.short, lawEgov:law.egov, article:n };
}

function getArticleList(lawId) {
  var keys = Object.keys(DB[lawId] || {});
  var nums = keys.filter(function(k) { return !isNaN(Number(k)); }).map(Number);
  nums.sort(function(a, b) { return a - b; });
  var strs = keys.filter(function(k) { return isNaN(Number(k)); });
  var result = [];
  for (var i = 0; i < nums.length; i++) {
    result.push(nums[i]);
    var n = nums[i];
    strs.filter(function(s) { return parseInt(s, 10) === n; }).sort().forEach(function(s) {
      result.push(s);
    });
  }
  return result;
}

